public class Contribution
{
    public static void main(String[] args)
    {
        final double INCOME = 6000.0;
        double contribution;
        //FIVEPERCENT
        contribution = INCOME * .05;
        System.out.println("A five percent contribution is " + contribution );

        contribution = .08 * INCOME;
        System.out.println("A Eight percent contribution is " + contribution );

        contribution = .1 * INCOME;
        System.out.println("A ten percent contribution is " + contribution );




    }

}
